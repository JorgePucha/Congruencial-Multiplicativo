    public boolean rPrimo(Double s, Double m) {
        Double may=m;
        if (s >= m) {
            may = s;
        } 
        int cont = 0;
        for (int i = 2; i <= may; i++) {
            if (s % i == 0 && m % i == 0) {
                cont++;
            }
        }
        return cont!=0;
    }
    public int mcm(int num1, int num2) {
        int mcm = 0;
        int a = Math.max(num1, num2);
        int b = Math.min(num1, num2);
        mcm = (a/mcd(a, b))*b;
        return mcm;
    }
     public static int mcd(int num1, int num2) {
        int mcd = 0;
        int a = Math.max(num1, num2);
        int b = Math.min(num1, num2);
        do {
            mcd = b;
            b = a%b;
            a = mcd;
        } while(b!=0);
        return mcd;
    }
